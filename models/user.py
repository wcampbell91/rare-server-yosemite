class User():
    def __init__(self, id, name, avatar, display_name, email, creation_date, user_type, password ):
        self.id = id
        self.name = name
        self.avatar = avatar
        self.display_name = display_name
        self.email = email
        self.creation_date = creation_date
        self.user_type = user_type
        self.password = password
